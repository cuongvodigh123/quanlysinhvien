package model;

public class GiangVien {
	private String id,ten;

	public GiangVien(String id, String ten) {
		super();
		this.id = id;
		this.ten = ten;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String tenkhoa) {
		this.ten = tenkhoa;
	}
	
}
